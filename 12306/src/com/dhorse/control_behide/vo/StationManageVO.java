package com.dhorse.control_behide.vo;
public class StationManageVO {
	private Integer stationId;	
	private String stationName;
	private String pinYin;
	private String stationDept;
	private Integer stationLev;
	private String policyDept;
	private String address;
	public Integer getStationId() {
		return stationId;
	}
	public void setStationId(Integer stationId) {
		this.stationId = stationId;
	}
	public String getStationName() {
		return stationName;
	}
	public void setStationName(String stationName) {
		this.stationName = stationName;
	}
	public String getPinYin() {
		return pinYin;
	}
	public void setPinYin(String pinYin) {
		this.pinYin = pinYin;
	}
	public String getStationDept() {
		return stationDept;
	}
	public void setStationDept(String stationDept) {
		this.stationDept = stationDept;
	}
	public Integer getStationLev() {
		return stationLev;
	}
	public void setStationLev(Integer stationLev) {
		this.stationLev = stationLev;
	}
	public String getPolicyDept() {
		return policyDept;
	}
	public void setPolicyDept(String policyDept) {
		this.policyDept = policyDept;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
}
