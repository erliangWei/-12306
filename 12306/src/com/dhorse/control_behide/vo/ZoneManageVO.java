package com.dhorse.control_behide.vo;

public class ZoneManageVO {
	private Integer zoneId;
	private Integer zoneFrom;
	private Integer zoneTo;
	private Integer zoneDistance;
	private Integer zoneAmount;
	public Integer getZoneId() {
		return zoneId;
	}
	public void setZoneId(Integer zoneId) {
		this.zoneId = zoneId;
	}
	public Integer getZoneFrom() {
		return zoneFrom;
	}
	public void setZoneFrom(Integer zoneFrom) {
		this.zoneFrom = zoneFrom;
	}
	public Integer getZoneTo() {
		return zoneTo;
	}
	public void setZoneTo(Integer zoneTo) {
		this.zoneTo = zoneTo;
	}
	public Integer getZoneDistance() {
		return zoneDistance;
	}
	public void setZoneDistance(Integer zoneDistance) {
		this.zoneDistance = zoneDistance;
	}
	public Integer getZoneAmount() {
		return zoneAmount;
	}
	public void setZoneAmount(Integer zoneAmount) {
		this.zoneAmount = zoneAmount;
	}
	

}
